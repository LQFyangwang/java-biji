package com.gs.relation;

/**
 * 发动机
 *
 */
public class Engine {

	private String brand;
	private int count;
	private int pi; // 马力多少匹
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getPi() {
		return pi;
	}
	public void setPi(int pi) {
		this.pi = pi;
	}
	
}

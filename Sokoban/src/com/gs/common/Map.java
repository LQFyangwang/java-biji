	public static int[][] copyMap(int[][] map) {
		int[][] newMap = new int[Constants.ROWS][Constants.COLS];
		for (int row = 0; row < Constants.ROWS; row++) {
			for (int col = 0; col < Constants.COLS; col++) {
				newMap[row][col] = map[row][col];
			}
		}
		return newMap;
	}
	
	public static void copyOMap(GamePanel gamePanel) {
		Stack<int[][]> maps = gamePanel.getMaps(); // 每走一步都会存储起来
		if (maps.size() > 0) {
			// 已经走了至少一步
			int[][] map = gamePanel.getMap(); // 改变了的地图
			int[][] oMap = maps.get(0); // 最初始的地图
			for (int row = 0; row < Constants.ROWS; row++) {
				for (int col = 0; col < Constants.COLS; col++) {
					map[row][col] = oMap[row][col];
				}
			}
		}
	}
	
